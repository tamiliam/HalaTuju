const PATHWAY_ICONS = {
  asasi: StarIcon,
  matric: GraduationCapIcon,
  stpm: BookOpenIcon,
  pismp: AcademicHeartIcon,
  poly: GearIcon,
  university: BuildingIcon,
  kkom: PeopleIcon,
  iljtm: WrenchIcon,
  ilkbs: HammerIcon,
}

export default function PathwayCards({ pathways }: { pathways: PathwaySummary[] }) {
  const { t } = useT()

  const eligible = pathways.filter(p => p.eligible)

  if (eligible.length === 0) return null

  return (
    <div className="mb-6">
      <h2 className="text-lg font-semibold text-gray-900 mb-3">
        {t('pathways.title')}
      </h2>
      <div className="flex flex-wrap gap-2">
        {eligible.map(p => {
          const Icon = PATHWAY_ICONS[p.type]
          return (
            <div
              key={p.type}
              className="flex items-center gap-2 bg-white rounded-lg border border-gray-200 shadow-sm px-3 py-2"
            >
              <Icon />
              <div>
                <span className="text-xs font-semibold text-gray-900">{p.label}</span>
                {p.count != null && (
                  <span className="text-[10px] text-gray-500 ml-1">
                    {p.count} {t('pathways.courses')}
                  </span>
                )}
                {p.detail && (
                  <span className="text-[10px] text-gray-500 ml-1">{p.detail}</span>
                )}
                {!p.count && !p.detail && (
                  <span className="inline-block ml-1 px-1.5 py-0.5 rounded-full text-[10px] font-medium bg-green-100 text-green-700">
                    {t('pathways.eligible')}
                  </span>
                )}
              </div>
            </div>
          )
        })}
      </div>
    </div>
  )
}
